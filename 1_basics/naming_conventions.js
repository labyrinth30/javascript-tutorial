/**
 * Naming Conventions
 * 
 * 1) 일반적으로 영어(라틴문자)를 사용하며 문자와 숫자를 모두 사용할 수 있다.
 * 2) 특수기호는 _와 $만 허용한다.
 * 3) 숫자로 시작할 수 없다.
 *    ex) 1test (X)
 * 4) 예약어는 사용할 수 없다.
 *   ex) var, function, if, else, for, while, ...
 * 
 */

